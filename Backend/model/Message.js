import mongoose from "mongoose";

const contactMessage = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        required: true
    },
    
});

const Message = mongoose.model("Contact", contactMessage)

export default Message;